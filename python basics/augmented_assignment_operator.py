# Take a random number
number = 10
print("Given value of number is: ", number)
# Increase it by 4, we can do it by
# number = 10 + 4
# OR by this method
number = number + 4
print("New value of number is: ", number)
number = number - 8
print("New value of number is: ", number)
number = number * 2
print("New value of number is: ", number)

# Augmented Assignment Operator
print("Using Augmented Assignment Operator")
number = 10
print("Given value of number is: ", number)
number += 4
print("New value of number is: ", number)
number -= 8
print("New value of number is: ", number)
number *= 2
print("New value of number is: ", number)
