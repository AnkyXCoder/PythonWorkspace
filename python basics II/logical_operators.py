# Logical Operators

# and   &
# or    |
# not   ~

# >
print ("4 > 5", 4 > 5)
print ("a > b", 'a' > 'b')
print ('A > a', 'A' > 'a')

# <
print ("4 < 5", 4 < 5)
print ("a < b", 'a' < 'b')
print ('A < a', 'A' < 'a')

# >=
print("1 >= 0", 1>=0)

# <=
print("1 <= 0", 1<=0)

# ==
print("5==5", 5==5)
print("True == 1", True == 1)
print("'' == 1", '' == 1)
print("[] == 1", [] == 1)
print("10 == 10.0", 10 == 10.0)
print("10 == 10.05", 10 == 10.05)
print("[] == []", [] == [])
print("[1] == [1]", [1] == [1])
print("[1,3] == [1,2]", [1,3] == [1,2])
print("[2,1] == [1,2]", [2,1] == [1,2])

# !=
print("5!=5", 5!=5)
print("5!=4", 5!=4)



print("1 < 2 < 3 < 4", 1 < 2 < 3 < 4)
print("1 < 2 > 3 < 4", 1 < 2 > 3 < 4)


# is
print("5 is 5", 5 is 5)
print("True  is  1", True  is  1)
print("'1'  is  1", '1'  is  1)
print("[1]  is  1", [1]  is  1)
print("10  is  10.0", 10  is  10.0)
print("10  is  10.05", 10  is  10.05)
print("[]  is  []", []  is  [])
print("[1]  is  [1]", [1]  is  [1])
print("[1,3]  is  [1,2]", [1,3]  is  [1,2])
print("[2,1]  is  [1,2]", [2,1]  is  [1,2])