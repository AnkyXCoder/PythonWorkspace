# Logical Operators

# and   &
# or    |

# >
print ("4 > 5", 4 > 5)
print ("a > b", 'a' > 'b')
print ('A > a', 'A' > 'a')

# <
print ("4 < 5", 4 < 5)
print ("a < b", 'a' < 'b')
print ('A < a', 'A' < 'a')

# >=
print("1 >= 0", 1>=0)

# <=
print("1 <= 0", 1<=0)

# ==
print("5==5", 5==5)

# !=
print("5!=5", 5!=5)
print("5!=4", 5!=4)



print("1 < 2 < 3 < 4", 1 < 2 < 3 < 4)
print("1 < 2 > 3 < 4", 1 < 2 > 3 < 4)
